//-----------------------------------------------------------
//                       EMPTY
//-----------------------------------------------------------
// Test if string is empty or not
bool string::empty(void) const
{
	bool isempty = 1;
	if(length_ == 0)
	{
		isempty= 0;
	} 
	return isempty;
}

//-----------------------------------------------------------
//                       DISPLAY
//-----------------------------------------------------------

void string::display(void)
{
	 int i=0;
	 for(i=0;i<length_;i++)
	 {
	 	printf("%c", character[i]);
	 }
	 printf("\n");
}
